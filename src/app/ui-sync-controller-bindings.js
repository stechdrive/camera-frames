export function createUiSyncControllerBindings({
	store,
	state,
	sceneState,
	viewportShell,
	renderBox,
	dropHint,
	fpsMovement,
	currentLocale,
	t,
	syncActiveShotCameraFromDocument,
	interactionController,
	updateOutputFrameOverlay,
	getSceneAssetCounts,
	getSceneBounds,
	getTotalLoadedItems,
	getActiveShotCamera,
	getActiveCamera,
	getProjectionState,
	projectionController,
	getActiveShotCameraDocument,
} = {}) {
	return {
		store,
		state,
		sceneState,
		viewportShell,
		renderBox,
		dropHint,
		fpsMovement,
		currentLocale,
		t,
		syncActiveShotCameraFromDocument,
		isZoomToolActive: () => interactionController?.isZoomToolActive() ?? false,
		updateOutputFrameOverlay,
		getSceneAssetCounts,
		getSceneBounds,
		getTotalLoadedItems,
		getActiveShotCamera,
		getActiveCamera,
		getProjectionState,
		getShotCameraPoseAngles: () =>
			projectionController?.getShotCameraPoseAngles?.() ?? {
				yawDeg: 0,
				pitchDeg: 0,
				rollDeg: 0,
			},
		getActiveShotCameraDocument,
	};
}
