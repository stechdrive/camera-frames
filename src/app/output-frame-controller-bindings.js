export function createOutputFrameControllerBindings({
	store,
	state,
	viewportShell,
	workbenchRightColumn,
	renderBox,
	renderBoxMeta,
	anchorDot,
	frameOverlayCanvas,
	outputFrameResizeHandles,
	workspacePaneCamera,
	isZoomToolActive,
	t,
	getAnchorLabel,
	currentLocale,
	getFrameController,
	getActiveShotCameraDocument,
	getShotCameraDocument,
	getActiveShotCameraEntry,
	shotCameraRegistry,
	getBaseFovX,
	updateActiveShotCameraDocument,
	updateUi,
	historyController,
} = {}) {
	return {
		store,
		state,
		viewportShell,
		workbenchRightColumn,
		renderBox,
		renderBoxMeta,
		anchorDot,
		frameOverlayCanvas,
		outputFrameResizeHandles,
		workspacePaneCamera,
		isZoomToolActive,
		t,
		getAnchorLabel,
		currentLocale,
		clearFrameSelection: () => getFrameController?.()?.clearFrameSelection?.(),
		isFrameSelectionActive: () =>
			getFrameController?.()?.isFrameSelectionActive?.() ?? false,
		getActiveShotCameraDocument,
		getShotCameraDocument,
		getActiveShotCameraEntry,
		shotCameraRegistry,
		getActiveFrames: () => getFrameController?.()?.getActiveFrames?.() ?? [],
		getFrameAnchorDocument: (frame) =>
			getFrameController?.()?.getFrameAnchorDocument?.(frame),
		resolveFrameAxis: (value) =>
			getFrameController?.()?.resolveFrameAxis?.(value),
		resolveFrameAnchor: (value, fallback) =>
			getFrameController?.()?.resolveFrameAnchor?.(value, fallback),
		getBaseFovX,
		updateActiveShotCameraDocument,
		updateUi,
		runHistoryAction: historyController.runHistoryAction,
		beginHistoryTransaction: historyController.beginHistoryTransaction,
		commitHistoryTransaction: historyController.commitHistoryTransaction,
		cancelHistoryTransaction: historyController.cancelHistoryTransaction,
	};
}
